"""Диаметр трубы, изготовленной на трубопрокатном цехе, представляет собой случайную величину, распределенную нормально с математическим ожиданием 15 см и средним квадратическим отклонением 0,4 см. Найти вероятность того, что размер наугад взятой трубы будет от 14 до 16 см. Ответ округлить до четырех знаков после запятой."""
from scipy.stats import norm

# Заданные параметры
mu = 15
sigma = 0.4

# Стандартизация интервала (14, 16)
z_lower = (14 - mu) / sigma
z_upper = (16 - mu) / sigma

# Вычисление вероятности
probability = norm.cdf(z_upper) - norm.cdf(z_lower)

# Вывод результата
print(f"Вероятность P(14 < X < 16): {probability:.4f}")
